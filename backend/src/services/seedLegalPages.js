const seedLegalPages = async () => {
    for (let i = 0; i < defaults.length; i++) {
        const row = defaults[i];
        const body_html = textToHtml(row.body_text);
        await db.execute(
            `INSERT IGNORE INTO legal_pages (slug, title, lead_text, body_text, body_html)
             VALUES (?, ?, ?, ?, ?)`,
            [row.slug, row.title, row.lead_text, row.body_text, body_html]
        );
    }

    const [rows] = await db.execute(
        'SELECT slug, body_html, body_text FROM legal_pages'
    );

    for (let i = 0; i < rows.length; i++) {
        const row = rows[i];
        if (row.body_text && String(row.body_text).trim() !== '') {
            continue;
        }
        const { htmlToText, textToHtml: toHtml } = require('../utils/legalTextFormat');
        const body_text = htmlToText(row.body_html);
        if (!body_text) {
            continue;
        }
        await db.execute(
            'UPDATE legal_pages SET body_text = ?, body_html = ? WHERE slug = ?',
            [body_text, toHtml(body_text), row.slug]
        );
    }
};

module.exports = seedLegalPages;
